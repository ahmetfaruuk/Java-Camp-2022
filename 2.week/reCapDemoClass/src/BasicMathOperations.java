public class BasicMathOperations {
    public int add(int n1, int n2) {
        return n1 + n2;
    }

    public int cikarma(int n1, int n2) {
        return n1 - n2;
    }

    public int carpma(int n1, int n2) {
        return n1 * n2;
    }

    public int bolme(int n1, int n2) {
        return n1 / n2;
    }
}
